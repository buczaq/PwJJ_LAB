    private void updateCatalogLabels()
    {
        if (catalogPosition > 4) catalogPosition = 0;
        if (catalogPosition < 0) catalogPosition = 4;
        jLabel1.setText(rb.getString("name") + ": " + rb.getString(ar.get(catalogPosition).get(0)));
        jLabel2.setText(rb.getString("price") + ": " + rb.getString(ar.get(catalogPosition).get(1)));
        jLabel3.setText(rb.getString("size") + ": " + rb.getString(ar.get(catalogPosition).get(2)));
        jLabel4.setText(rb.getString("color") + ": " + rb.getString(ar.get(catalogPosition).get(3)));
        jButton5.setText(rb.getString("previous"));
        jButton6.setText(rb.getString("next"));
        int tmp = catalogPosition + 1;
        String s = "item" + tmp + "_image";
        String imageName = rb.getString(s);
        ImageIcon icon = new ImageIcon(imageName);
        icon.getImage().flush();
        image.setIcon(icon);
        itemsGUI.setVisible(true);
    }

