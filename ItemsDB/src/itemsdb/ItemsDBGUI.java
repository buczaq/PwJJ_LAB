    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String n = ar.get(catalogPosition).get(0);
        String p = ar.get(catalogPosition).get(1);
        String s = ar.get(catalogPosition).get(2);
        String c = ar.get(catalogPosition).get(3);
        ArrayList<String> i = new ArrayList<String>();
        i.add(n);
        i.add(p);
        i.add(s);
        i.add(c);
        try {
            ia.addItem(ar, i);
        } catch (ParserConfigurationException ex) {
            Logger.getLogger(ItemsDBGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SAXException ex) {
            Logger.getLogger(ItemsDBGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ItemsDBGUI.class.getName()).log(Level.SEVERE, null, ex);
        } catch (TransformerException ex) {
            Logger.getLogger(ItemsDBGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

