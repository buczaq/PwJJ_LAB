    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String[][] rowData = new String[ar.size()][4];
        for (int i = 0; i < rowData.length; i++) {
            for (int j = 0; j < rowData[i].length; j++) {
                rowData[i][j] = rb.getString(ar.get(i).get(j));
            }
        }
        
        String[] colNames = new String[] { rb.getString("name"), rb.getString("price"), rb.getString("size"), rb.getString("color") };
        
        jTable1.setModel(new javax.swing.table.DefaultTableModel(rowData, colNames));
        itemListGUI.setVisible(true);
        
    }//GEN-LAST:event_jButton1ActionPerformed

